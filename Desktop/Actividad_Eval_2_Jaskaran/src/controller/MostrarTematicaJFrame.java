    public void mostrarTematica(){
     DefaultTableModel model = (DefaultTableModel) jTableMostrarTematicas.getModel();
        for (Tematica tematica : this.tematicaServer.mostrarTematicas()) {
            model.addRow(new Object[]{tematica.getNombre(),tematica.getFechaDeAlta(),tematica.getFechaDeModificacion()});
        }
    }

