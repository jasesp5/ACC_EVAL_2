    private void jButtonModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonModificarActionPerformed
        // TODO add your handling code here:
        if (jTextFieldNombre.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, NO_PUEDE_DEJAR_EL_CAMPO_VACIO);
        } else if (this.tematicaServer.modificarTematica((String) jComboBox1.getSelectedItem(), this.jTextFieldNombre.getText())) {
            JOptionPane.showMessageDialog(null, MODIFICADO_CORRECTAMENTE);
            this.dispose();
            ModificarTematicaJFrame nuevoVentanaModificar = new ModificarTematicaJFrame();
            nuevoVentanaModificar.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(null, TEMATICA_YA_EXISTE);
            this.jTextFieldNombre.setText("");
        }
    }//GEN-LAST:event_jButtonModificarActionPerformed

